/**
 * What is JavaScript?
 * JavaScript is a lightweight interpreted language, which historically has been
 * used for manipulating browser content to create dynamic web pages. JavaScript
 * is dynamic and loosely-typed langauge. 
 * 
 * Loosely-Typed: Types are associated with values and not with variables.
 * 
 * What are some advantages to using JavaScript?
 *  * Fast Development
 *  * Don't need complicated dev environment
 *  * Front-end development requires it
 *  * Back-end options also exist, which means we can work both front-end
 *         and back-end with the same language
 *  * Lots of community support, lots of open source content
 *  * Interpreted language is quicker to run and test
 * 
 *  What are some disadvantages?
 *  * Loosely typed means we are responsible for managing our types
 *  * There is no uniform module package that works cross-platform
 *  * Browser code is public code
 *  * Interpreted language may not execute as efficiently as a compiled language
 */