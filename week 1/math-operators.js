let x = 10;
let y = 5;

console.log(x + y); // 15
console.log(x - y); //  5
console.log(x * y); // 50
console.log(x / y); //  2
console.log(x % y); //  0

console.log(Math.PI);
// % - modulus operator
// remainder from integer division

// increment/decrement operators
let z = 0;
z++;
console.log(z); // 1
z--;
console.log(z); // 0

// Shorthand reassignment operators

z += 10; // Shorthand for z = z + 10;
z *= 2;  // shorthand for z = z * 2;
// also -=, /=, %=
console.log(z);

z %= 1;
console.log(z);

console.log(Math.pow(10, 2));